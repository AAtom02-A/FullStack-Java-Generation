/* Operadores relacionales o de comparacion
    > mayor que
    < Menor que
    >= Mayor o igual
    <= Menor o igual 
    == Igual  
    != Desigual
    === Operador estricto 
    !== Operador diferente estricto

    Al usar operadores ligicos obtenemos un booleano true o false. 
*/

let numero1 = 22;
let numero2 = 15;

console.log("Mayor que " + (numero1 > numero2));
console.log("Menor que " + (numero1 < numero2));
console.log("Mayor o igual " + (numero1 >= numero2));
console.log("Menor o igual " + (numero1 <= numero2));
console.log("Igual " +(numero1 == numero2));
console.log("Desigual " + (numero1 != numero2));


